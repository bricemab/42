#include "../include/libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*prev;
	t_list	*tmp;

	if (lst && *lst && del)
	{
		prev = (t_list *)*lst;
		while (prev)
		{
			tmp = prev->next;
			ft_lstdelone(prev, del);
			prev = tmp;
		}
		*lst = NULL;
	}
}
