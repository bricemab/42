#include "libft.h"

/*
	If 'c' is an uppercase letter, the function returns its 
	lowercase equivalent otherwise it returns 'c'.
*/

int	ft_tolower(char c)
{
	if (ft_isupper(c))
		c += 32;
	return (c);
}
