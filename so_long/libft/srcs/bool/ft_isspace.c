#include "libft.h"

/*
	Checks if 'c' is a space (\t, \n, \v, \f, \r)
*/

int	ft_isspace(int c)
{
	if ((c >= '\t' && c <= '\r') || c == ' ')
		return (1);
	return (0);
}
