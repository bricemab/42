#include "libft.h"

/*
	If 'c' is a lowercase letter, the function returns its 
	uppercase equivalent otherwise it returns 'c'.
*/

int	ft_toupper(char c)
{
	if (ft_islower(c))
		c -= 32;
	return (c);
}
