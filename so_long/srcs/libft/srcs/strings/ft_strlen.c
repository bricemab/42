#include "libft.h"

/*
	Calculates the length of the string pointed to by 's', excluding the 
	terminating null byte '\0'.
*/

size_t	ft_strlen(const char *s)
{
	size_t	len;

	len = 0;
	while (s[len])
		len++;
	return (len);
}
