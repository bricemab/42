#include "libft.h"

/*
	Frees the allocated memory of the node passed as parameter using
	the functions del() and free()
*/

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (lst && del)
	{
		del(lst->content);
		free(lst);
	}
}
